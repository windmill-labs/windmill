import { redirect } from '@sveltejs/kit'
import { base } from '$app/paths'
import { getUsernameForNamespace } from '$lib/userNamespace'
import { randomUUID } from '$lib/utils/uuid'

/**
 * Shared `load` for every `/{scripts,flows,apps,apps_raw}/add` route.
 *
 * Each route used to mount its `+page.svelte` and `goto` from `onMount` —
 * painting one blank frame before navigating, hence the white flash.
 * Doing the work in `load` lets SvelteKit cancel the route transition
 * and head straight for the edit page with no DOM round-trip.
 *
 * Mints a fresh `u/<username>/draft_<uuid>` path and issues a 307 to
 * `{base}/{editPrefix}/<that-path>?new_draft=true&<existing-params>`.
 * The `new_draft=true` flag tells the edit route to seed an empty
 * editor instead of 404-ing on the autogenerated path. The URL hash is
 * carried over too — the run page's "Fork" action and
 * workspace_settings' handler-template buttons encode a script payload
 * into it, and the edit route's `new_draft` branch consumes it.
 *
 * Uses the project's `randomUUID` helper rather than
 * `crypto.randomUUID()` — the WebCrypto version is unavailable on
 * non-secure origins (HTTP, local IPs over LAN), and self-hosted
 * Windmill instances frequently run that way.
 */
export function makeDraftAddLoad(editPrefix: string) {
	return ({ url }: { url: URL }) => {
		const username = getUsernameForNamespace()
		// Underscores, not dashes — path segments elsewhere in Windmill are
		// `[a-zA-Z0-9_]` words, and downstream consumers (path validators,
		// friendly-name slugs) treat `-` as a foreign character.
		const uuid = randomUUID().replaceAll('-', '_')
		const params = new URLSearchParams(url.searchParams)
		params.set('new_draft', 'true')
		// SvelteKit forbids `url.hash` inside `load` (loads also run where
		// no hash exists), but this app is SPA-only (`ssr = false` in the
		// root layout) so `window` is always live here. Caveat: during an
		// in-app (client-side) navigation `window.location` still points at
		// the PREVIOUS page — that's fine because every hash-payload
		// producer (run page fork via `window.open`, workspace_settings'
		// `target="_blank"` template buttons) arrives as a full page load.
		const hash = typeof window !== 'undefined' ? window.location.hash : ''
		redirect(307, `${base}/${editPrefix}/u/${username}/draft_${uuid}?${params.toString()}${hash}`)
	}
}
