export function ordersInRange(orders: Order[], from: string, to: string): Order[] {
	return orders.filter((order) => {
		const day = order.placedAt.slice(0, 10)
		return day >= from && day <= to
	})
}

export function ordersForRegion(orders: Order[], region: string): Order[] {
	if (region === 'all') {
		return orders
	}
	return orders.filter((order) => order.region === region)
}

